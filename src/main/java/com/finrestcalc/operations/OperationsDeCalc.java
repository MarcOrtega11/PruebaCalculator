package com.finrestcalc.operations;

public class OperationsDeCalc {
	
	public double suma(double number1, double number2) {
		return number1 + number2;		
	}
	
	public double resta(double number1, double number2) {
		return number1 - number2;		
	}
	
	public double multi(double number1, double number2) {
		return number1 * number2;		
	}
	
	public double divi(double number1, double number2) {
		return number1 / number2;		
	}

}
